// Переразметка истории: «Выплаты ЗП», которые на самом деле были изъятиями владельца.
//
// До появления вида операции «Изъятие» личные траты проводили как зарплату
// с комментарием («Продукты», «Дом»), а настоящую выплату педагогу — без него.
// Правило рабочее, но нигде не записанное: комментарий у зарплаты ничего не значил,
// и однажды приписанное примечание к настоящей выплате сломало бы отчёт.
//
// Поэтому переразметка не делается автоматически по комментарию: модуль лишь
// ПРЕДЛАГАЕТ кандидатов, а решает владелец галочками. Дальше вид операции есть
// в форме, и догадываться уже не нужно.

import { KIND_SALARY, KIND_DRAW, toJsDate } from './finance'

// Кандидат — выплата ЗП с непустым комментарием. Уже переразмеченные (kind 'draw')
// сюда не попадают: панель показывает только то, что ещё предстоит решить.
export const isDrawCandidate = (t) =>
  t.kind === KIND_SALARY && Boolean(String(t.comment || '').trim())

export function drawCandidates(transactions, { teacherId = '' } = {}) {
  return transactions
    .filter(isDrawCandidate)
    .filter(t => !teacherId || t.teacherId === teacherId)
    .sort((a, b) => (toJsDate(b.date)?.getTime() || 0) - (toJsDate(a.date)?.getTime() || 0))
}

// Что записать в операцию, ставшую изъятием. Педагога снимаем: изъятие — не выплата
// сотруднику, и оставшийся teacherId испортил бы отчёт «Выплаты по педагогам».
// Статья меняется на «Изъятие владельца»: у старой статьи тип salary, и в форме
// правки она бы не нашлась среди статей изъятия.
export const toDrawDoc = (categoryId) => ({
  kind: KIND_DRAW,
  categoryId,
  teacherId: '',
  teacherName: '',
})

// Сколько денег вернётся в прибыль, если перевести отмеченные операции.
export const drawSum = (transactions) =>
  transactions.reduce((total, t) => total + (Number(t.amount) || 0), 0)
